// Declaring variables in JavaScript serves the purpose of reserving memory space to store data or values that can be used and manipulated within a program. It allows developers to reference and work with these values using meaningful names. Declaring variables using the let keyword is one of the modern ways to define variables in JavaScript.

let age = 25; // Declaring a variable 'age' and assigning it a value of 25
