// Question 6: Explain the importance of choosing meaningful and descriptive variable names in Javascript.
// Provide an example where using a clear identifier improves code readability.

// Choosing meaningful and descriptive variable names in JavaScript significantly enhances code readability and maintainability.

// 1.Clarity and Understanding        2.Reduced Cognitive Load    3.Easier Maintenance

// Less descriptive variable name
let a = 10;
let b = 20;
let c = a + b;

// More descriptive variable name
let firstNumber = 10;
let secondNumber = 20;
let sum = firstNumber + secondNumber;

// In this example, using firstNumber and secondNumber instead of a and b makes it immediately clear what values these variables hold. Additionally, using sum instead of c clearly indicates the purpose of the calculated value. The more descriptive variable names significantly improve code readability and understanding, especially when someone else (or even the original coder after some time) reviews the code.
